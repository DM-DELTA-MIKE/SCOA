    private void AtualizarAlunosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarAlunosButtonActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) this.tabelaAlunos.getModel();
        AlunoController controller = (AlunoController) Dashboard.springAppContext.getBean("alunoController");
        
        List<AlunoModel> alunos = controller.getAll().getBody();
        
        model.setRowCount(0);
        
        //E-mail, id, ano letivo, carga horaria, matricula, situação
        for(AlunoModel aluno : alunos){
            model.addRow(new Object[]{
                aluno.getUsuario() != null ? aluno.getUsuario().getId() : "",
                aluno.getId(),
                aluno.getAno_letivo(),
                aluno.getCarga_horaria(),
                aluno.getMatricula(),
                aluno.getSituacao()
            });
        }
        
    }//GEN-LAST:event_AtualizarAlunosButtonActionPerformed

