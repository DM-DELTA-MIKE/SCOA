    private void AtualizarUsuariosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarUsuariosButtonActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) this.TabelaUsuarios.getModel();
        
        UsuarioController controller = (UsuarioController) Dashboard.springAppContext.getBean("usuarioController");
        
        List<UsuarioModel> usuarios = controller.getAll().getBody();
        
        model.setRowCount(0);
        
        
        //E-mail, CPF, telefone, username
        for(UsuarioModel usuario : usuarios){
            model.addRow(new Object[]{
                usuario.getId(),
                usuario.getCpf(),
                usuario.getTelefone(),
                usuario.getUsername()
            });
        }
    }//GEN-LAST:event_AtualizarUsuariosButtonActionPerformed

